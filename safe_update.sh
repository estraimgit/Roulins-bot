echo -e "${GREEN}‚úÖ –î–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ —Å–æ–∑–¥–∞–Ω—ã${NC}"

# 11. –ó–∞–ø—É—Å–∫–∞–µ–º –±–æ—Ç–∞
echo -e "\n${YELLOW}üöÄ –®–∞–≥ 11: –ó–∞–ø—É—Å–∫ –æ–±–Ω–æ–≤–ª–µ–Ω–Ω–æ–≥–æ –±–æ—Ç–∞...${NC}"
