echo "  –õ–æ–≥–∏: logs/bot_output.log"
echo "  –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è: .env"
echo ""
echo "üîß –ü–æ–ª–µ–∑–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã:"
echo "  –ü—Ä–æ—Å–º–æ—Ç—Ä –ª–æ–≥–æ–≤: tail -f logs/bot_output.log"
echo "  –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –±–æ—Ç–∞: kill $BOT_PID"
echo "  –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç–∞—Ç—É—Å–∞: ps -p $BOT_PID"
echo ""
echo "üíæ –†–µ–∑–µ—Ä–≤–Ω—ã–µ –∫–æ–ø–∏–∏:"
echo "  –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö: $BACKUP_DIR"
echo "  –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è: .env.backup.*"
echo ""

